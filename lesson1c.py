# Numbers
# Arithmetic operators

num1 = 40
num2 = 10

# Addition
sum = num1 + num2
print("Addition of ", num1, " and", num2, "is: ", sum)

print("==============================================")

# subtraction
sub = num1 - num2
print("difference of ", num1, " and", num2, "is: ", sub)

print("==============================================")

# Multiplication
product = num1 * num2
print("product of ", num1, " and", num2, "is: ", product)

print("==============================================")

# Division
quotient = num1 / num2
print("quotient of ", num1, " and", num2, "is: ", quotient)

print("==============================================")

# Modulus
modulus = num1 % num2
print("Modulus of ", num1, " and", num2, "is: ", modulus)

print("==============================================")

# Exponent
exponent = num1 ** num2
print("Exponent of ", num1, " and", num2, "is: ", exponent)

print("==============================================")

# Floor division- rounds of to whole number
round = num1 // num2
print("Floor Division of ", num1, " and", num2, "is: ", round)